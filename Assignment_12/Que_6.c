// Purpose - To print first 10 natural numbers in reverse order

// QUESTION.
//     WAP to print first ‘10’ natural numbers in reverse order.

#include <stdio.h>
int main()
{
    for (int i = 10; i >= 1; i--)
    {
        printf("%d,", i);
    }
    return 0;
}