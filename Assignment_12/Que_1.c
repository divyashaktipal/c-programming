// Purpose - To print Hello World 'N' number of times using loop method.

// Question.
//      WAP to print Hello World on screen N number of times.

#include <stdio.h>
int main()
{
    int a;
    while (a > 0)
    {
        printf("Hello world \n");
        a = a - 1;
    }

    return 0;
}