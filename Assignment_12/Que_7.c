// Purpose - To print 'N' natural number in reverse order using loop method.

// QUESTION.
//      WAP to printf'N' natural numbers in reverse order.

#include <stdio.h>
int main()
{
    int i;
    printf("enter any intger \n");
    scanf("%d", &i);

    for (i >= 1; i >= 1; i--)
    {
        printf("%d ,", i);
    }

    return 0;
}